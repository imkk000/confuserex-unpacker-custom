        public static readonly Dictionary<int, OpInfo> ByKey = BuildByKey();

        private static Dictionary<int, OpInfo> BuildByKey()
        {
            Dictionary<int, OpInfo> map = new Dictionary<int, OpInfo>();
            foreach (string line in RawEntries.Split('\n'))
            {
                string trimmed = line.Trim();
                if (trimmed.Length == 0)
                {
                    continue;
                }
                string[] parts = trimmed.Split('|');
                int key = int.Parse(parts[0]);
                byte opType = byte.Parse(parts[1]);
                string handler = parts[2];

                OpInfo info = new OpInfo { Key = key, OperandType = opType, Handler = handler };
                info.IsGenericStind = GenericStindHandlers.Contains(handler);
                info.IsUnifiedCall = handler == "_0008_200A";
                if (HandlerToCil.TryGetValue(handler, out OpCode cil))
                {
                    info.Cil = cil;
                    info.IsNewobjCandidate = cil.Code == Code.Call || cil.Code == Code.Callvirt;
                }
                else
                {
                    info.Cil = OpCodes.UNKNOWN1;
                }
                map[key] = info;
            }
            return map;
        }

